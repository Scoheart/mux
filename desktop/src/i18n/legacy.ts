function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

const templatePatterns = Object.entries(english).flatMap(([source, target]) => {
  const placeholders = [...source.matchAll(/\$\{[^}]+\}/g)].map((match) => match[0]);
  if (placeholders.length === 0) return [];
  const segments = source.split(/\$\{[^}]+\}/g).map(escapeRegExp);
  const pattern = new RegExp(`^${segments.join("(.+?)")}$`);
  return [{
    pattern,
    render(matches: RegExpMatchArray) {
      return placeholders.reduce((rendered, placeholder, index) =>
        rendered.replaceAll(placeholder, matches[index + 1] ?? ""), target);
    },
  }];
});

export function localizeLegacyText(source: string, locale: SupportedLocale): string {
  if (locale === "zh-CN" || !source.trim()) return source;
  const leading = source.match(/^\s*/)?.[0] ?? "";
  const trailing = source.match(/\s*$/)?.[0] ?? "";
  const core = source.trim();
  const exact = english[core];
  if (exact) return `${leading}${exact}${trailing}`;
  for (const template of templatePatterns) {
    const match = core.match(template.pattern);
    if (match) return `${leading}${template.render(match)}${trailing}`;
  }
  for (const [pattern, render] of patterns) {
    const match = core.match(pattern);
    if (match) return `${leading}${render(...match)}${trailing}`;
  }
  return source;
}

